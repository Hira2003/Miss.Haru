// Function to get a random dua
function getRandomDua() {
    const randomIndex = Math.floor(Math.random() * duaArray.length);
    return duaArray[randomIndex];
}

// Load and display random dua when page loads
document.addEventListener('DOMContentLoaded', function() {
    const duaElement = document.getElementById('randomDua');
    if (duaElement) {
        duaElement.textContent = getRandomDua();
    }
});
